

def pyramid():
  for i in range(1,5):
   print(' ',end = '')
   if i==4:
    print('*')
    

  for i in range(1,4):
   print(' ',end ='')
   if i==3:
    print('*' * 3)


  for i in range(1,3):
   print(' ',end ='')
   if i==2:
    print('*' * 5) 
    

  for i in range(1,2):
   print(' ',end ='')
   if i==1:
    print('*' * 7)
    

    
    
pyramid()    
pyramid() 


